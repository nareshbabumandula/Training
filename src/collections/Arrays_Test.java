package collections;

public class Arrays_Test {

	public static void main(String[] args) {
		
		int i[]={15,10,25,78};
		
		//get number of values in array
		System.out.println("Values in array are : "+i.length);
		//get 3rd value from array
		System.out.println("3rd value from array : "+i[2]);

		//print all the values from array
		for (int j = 0; j < i.length; j++) {
			System.out.println("Values in array are : "+i[j]);
			
		}
		

	}

}
